fn main() {
    // let x = immutable variable
    let x = 5;
    println!("The value of x is: {x}");
    // x = 6 ;
    println!("The value of x is: {x}");

    // let mut y = mutable variable
    let mut y = 5;
    println!("The value of y is: {y}");
    y = 6;
    println!("The value of y is: {y}");
}
